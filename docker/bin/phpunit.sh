#!/bin/bash

set -e

: "${REEL_PHPUNIT_DIR:=.}"
: "${REEL_PHPUNIT_DOCKER_BUILD_CONTEXT:=.}"
: "${REEL_PHPUNIT_DOCKER_BUILD_ARGS:=--rm --pull}"
: "${REEL_PHPUNIT_DOCKER_FILE_NAME:=Dockerfile}"
: "${REEL_PHPUNIT_DOCKER_FILE_TARGET:=dev-phpunit}"

_ARGS_=()

while [ $# -gt 0 ]; do
  case "$1" in
  --REEL_PHPUNIT_DIR=*)
    REEL_PHPUNIT_DIR="${1#*=}"
    ;;
  --REEL_PHPUNIT_DOCKER_BUILD_CONTEXT=*)
    REEL_PHPUNIT_DOCKER_BUILD_CONTEXT="${1#*=}"
    ;;
  --REEL_PHPUNIT_DOCKER_BUILD_ARGS=*)
    REEL_PHPUNIT_DOCKER_BUILD_ARGS="${1#*=}"
    ;;
  --REEL_PHPUNIT_DOCKER_FILE_NAME=*)
    REEL_PHPUNIT_DOCKER_FILE_NAME="${1#*=}"
    ;;
  --REEL_PHPUNIT_DOCKER_FILE_TARGET=*)
    REEL_PHPUNIT_DOCKER_FILE_TARGET="${1#*=}"
    ;;
  *)
    _ARGS_+=($1)
    ;;
  esac
  shift
done

: "${REEL_DOCKERCEPTION_BUILD_CONTEXT:=.}"
: "${REEL_DOCKERCEPTION_FILE_NAME:=Dockerfile}"
: "${REEL_DOCKERCEPTION_FILE_TARGET:=dev-phpunit}"
: "${REEL_DOCKERCEPTION_RUN_DIR:=.}"
: "${REEL_DOCKERCEPTION_RUN_WORKDIR:=/app}"

_ARGS_RUN_=()
_ARGS_RUN_+=("--REEL_DOCKERCEPTION_BUILD_TAG=phpunit")
_ARGS_RUN_+=("--REEL_DOCKERCEPTION_BUILD_CONTEXT=$REEL_PHPUNIT_DOCKER_BUILD_CONTEXT")
_ARGS_RUN_+=("--REEL_DOCKERCEPTION_FILE_NAME=$REEL_PHPUNIT_DOCKER_FILE_NAME")
_ARGS_RUN_+=("--REEL_DOCKERCEPTION_FILE_TARGET=$REEL_PHPUNIT_DOCKER_FILE_TARGET")
_ARGS_RUN_+=("--REEL_DOCKERCEPTION_RUN_DIR=$REEL_PHPUNIT_DIR")
_ARGS_RUN_+=("--REEL_DOCKERCEPTION_RUN_WORKDIR=/app")

dockerception-build-and-run ${_ARGS_RUN_[*]} phpunit ${_ARGS_[*]}
